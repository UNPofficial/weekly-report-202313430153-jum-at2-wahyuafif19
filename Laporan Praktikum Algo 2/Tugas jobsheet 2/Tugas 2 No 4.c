#include <stdio.h>

int main() {
    // Deklarasi variabel
    float panjang, lebar, luas;

    // Assign nilai panjang dan lebar
    panjang = 10.0;
    lebar = 5.0;

    // Menghitung luas persegi panjang
    luas = panjang * lebar;

    // Menampilkan hasil
    printf("Panjang: %.2f cm\n", panjang);
    printf("Lebar: %.2f cm\n", lebar);
    printf("Luas: %.2f cm^2\n", luas);

    return 0;
}

